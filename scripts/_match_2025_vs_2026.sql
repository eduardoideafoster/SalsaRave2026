matches AS (
  SELECT
    ly.room_num AS room_2025,
    g.id AS guest_id,
    g.full_name,
    g.order_code,
    COUNT(*) AS token_matches
  FROM year2025 ly
  JOIN guests g ON g.full_name ILIKE '%' || ly.token || '%'
  GROUP BY ly.room_num, g.id, g.full_name, g.order_code
  HAVING COUNT(*) >= 2
),
with_room AS (
  SELECT
    m.*,
    r.room_number AS room_2026
  FROM matches m
  LEFT JOIN bookings b ON b.guest_id = m.guest_id AND b.status <> 'cancelled'
  LEFT JOIN rooms r ON r.id = b.room_id
)
